<h2 class="hcls">Call for Tutorials</h2>

<p>
We seek to extend the SIGCOMM experience by tutorials on selected topics given by renowned scientists and practitioners in their fields. We therefore solicit proposals for full-day or half-day tutorials on topics relevant to the SIGCOMM community.
</p>

<p>
Tutorials must cover advanced topics that fit the scope of SIGCOMM and are of current interest to the SIGCOMM community. Tutorials may be lectures, interactive workshops, hands-on training, or any combination of the above. Exploring diverse ways of interacting with the audience is welcome as are cross-disciplinary topics.</p>

<p>
Tutorial proposals should be submitted in PDF format, should not exceed three (3) pages in total, and be sent to Sonia Fahmy (<a href="mailto:fahmy@cs.purdue.edu">fahmy@cs.purdue.edu</a>) and Jia Wang (<a href="mailto:jiawang@research.att.com">jiawang@research.att.com</a>).
</p>

<p>
Proposals must include:
</p>

<ul>

<li>motivation for the tutorial (why this, why now, why at SIGCOMM?)</li>
<li>an outline of the tutorial content, including its tentative schedule (over the half-day or full-day)</li>
<li>the type of tutorial (e.g., lecture vs. hands-on)</li>
<li>references to previous iterations of the tutorial (if applicable) including their date, venue, topics and number of participants and the motivation for the new proposal </li>
<li>requirements for the tutorial room (please note that our capabilities in fulfilling unusual requests are limited)</li>
<li>requirements for the attendees (e.g., must bring own laptop or other hardware, familiarity with certain technologies or topics, etc.)</li>
<li>any limitations (e.g., number of participants)</li>

</ul>

<p>Important notes:</p>

<ul>
<li>It is recommended that there are up to three speakers for a full-day tutorial and up to two speakers for a half-day tutorial. Tutorial speakers are expected to hold a PhD degree in computer science or a related area. Exceptions will need to get approval from the tutorial co-chairs.</li>
<li>Tutorials will likely be held in parallel with the workshops, i.e., Monday and/or Friday of the SIGCOMM week.</li>
<li>Upon acceptance of a tutorial proposal, either the tutorial co-chairs or the tutorial speakers can request a cancelation of the tutorial if there is clear evidence indicating low attendance of the tutorial by the early registration deadline.</li>
</ul>

