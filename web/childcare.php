<h2 class=hcls>Frequently Asked Questions</h2>

<ul>
	<li><b>How old/young can my child be to register?</b>
SIGCOMM Kiddie is for children ages 6 month through 12 years old.</li>

 	<li><b>How much does it cost? When is it available?</b> See dates and fees above.</li>

	<li><b>Where will SIGCOMM Kiddie located?</b>  It will be conveniently located in a conference room in the main hotel, making it easy for you to check on your child while keeping them away for the craziness of a conference.</li>

 	<li><b>What will my child be doing at day care?</b> Some of the activities planned include exciting themes, arts and crafts, group games, music and movement, board games, story time and dramatic play. There are activities appropriate for each age group!</li>

 	<li><b>Do I need to bring anything for my child?</b> We will provide snacks and beverage. Parents will be responsible for supplying all meals for their children. For parents with infants, please bring diaper changing supplies, formula/baby food, and a change of clothes. Medication will NOT be administered by day care staff.</li>

 	<li><b>How many childcare providers will be in the rooms?</b> There will be high staff to child ratios to ensure every child feels special (1:2 for children age 6-11 months, 1:3 for children age 1-2 years old, 1:5 for children age 3-12 years old).</li>

</ul>

