<h1 class=hcls>Venue</h1>

<img src="http://static.panoramio.com/photos/large/96673470.jpg" width=300  class="photo-right">

<p>This year, SIGCOMM will take place in the historic Palmer House Hilton hotel. The Palmer House Hilton is centrally located downtown Chicago, within convenient walking distance of a vast number of cultural and culinary attractions, as well as some of the world's oldest and tallest skyscrapers. The conference reception will be held in the hotel. </p>


<h2 class=hcls>Banquet Dinner</h2>

<img src="http://www.sheddaquarium.org/Global/ALL_SHEDD%20PAGE%20PHOTOS/1.%20Plan%20a%20Visit/Plan%20a%20Private%20Event/PAV_CaribbeanReef_400x250_D.jpg" width=300  class="photo-left">

<p>For the conference banquet, we have rented out the Shedd Aquarium, once the largest aquarium in the world, and still visited by over 2 million people annually. The Shedd aquarium is a pleasant park stroll away from the conference venue.</p>

<p>The walk back to the Palmer House is 32 minutes in a (hopefully) balmy summer night. The area is safe for walking, but if you're not in the mood, the CTA buses <a href="http://www.transitchicago.com/assets/1/bus_schedules/130.pdf" rel="external">#130</a> and <a href="http://www.transitchicago.com/assets/1/bus_schedules/146.pdf" rel="external">#146</a> serve a direct route between the aquarium and the hotel, until 11:30 pm. Please note that bus drivers do not make change. A taxi should be around $10.</p>


<h2 class=hcls>Student Dinner</h2>

<img src="http://www.freshtechmaids.com/wp-content/uploads/2013/12/Signature-Room1.jpg" width=300 class="photo-right">

<p>Finally, the student dinner, where students get their own chance to mingle with senior researchers, will be held in the Signature Lounge, on the 95th floor of the John Hancock tower. Many feel that the Hancock tower offers views clearly superior to those from the even taller Sears/Willis tower, due to its superior location by the lake, and the Magnificent Mile. </p>


