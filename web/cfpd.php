<h1 class="hcls">ACM SIGCOMM 2014 Call for Posters, Demos, and Student Research Competition</h1>

<p>The SIGCOMM poster/demo sessions showcase works-in-progress in an informal setting. Topics of interest are the same as research topics in the SIGCOMM conference call for papers. <i>We strongly encourage <b>student</b> and <b>industry</b> submissions</i>. The SIGCOMM 2014 Poster and Demo committee will review all posters and demo proposals. Students must present student posters at the conference. Authors of accepted papers in SIGCOMM 2014 may not submit a poster on the same work in the paper.</p>

<h2 class="hcls">ACM Student Research Competition (SRC) </h2>
<p>The SIGCOMM poster session will also serve as an ACM student research competition. Qualified entrants must have current ACM student membership, have graduate or undergraduate student status at the time of submission (May 2014), and be the only student working on the project. While not mandatory, entrants are encouraged to submit a letter from their advisor describing the specific contributions made by the student. Undergraduates and graduate students will be treated in separate divisions (students starting their first year of graduate school at the time of the conference will be considered as undergraduates). A small travel supplement is made available to accepted SRC entrants; please also submit applications for travel grant support. Winners will advance to ACM Grand Finals of the Student Research Competition to compete against the winners of other ACM conferences. The SRC is sponosored by Microsoft Research.</p>

<h2 class="hcls">Why Should You Submit A Poster? </h2>
<p>Presenting a poster is a great opportunity, especially for students, to obtain interesting and valuable feedback on ongoing research from a knowledgeable crowd at the conference. In addition, the top few submissions will be forwarded for publication to the SIGCOMM newsletter, the Computer Communication Review (CCR). Students who are submitting posters are highly encouraged to examine if they are eligible for student travel grants.</p>

<h2 class="hcls">What Is A Poster? </h2>
<p>A poster is A0 paper size in portrait mode (841 &times; 1189mm), to which you can affix visually appealing material that describes your research. Alternatively, you can use the space as a continuum. You should prepare the best material (visually appealing and succinct) that effectively communicates your research problem, techniques, results, and what is novel/important about the work.</p>
You do not submit such a large-format image; only an abstract describing in text what the poster would present. The abstract should clearly state: (a) the problem being addressed, (b) what makes this problem interesting, important, and difficult, (c) your approach to the problem, and (d) the key contribution. In the final version of the abstract, you should include a URL that provides additional information about your work to the attendees.</p>

<h2 class="hcls">What To Submit </h2>
<p>You must submit a two-page abstract in PDF format that describes your work. Prepare your abstract using <a href="http://www.acm.org/sigs/publications/proceedings-templates">ACM SIG Alternate conference style</a>. Word documents will not be accepted. We will distribute the abstracts to all attendees at the conference.
It is highly encouraged that each Demo proposal includes a video clip showcasing the demo, in addition to the abstract. The video should be no more than 1 minute and should give a good idea of what the demo is about and what it would look like. Including a video clip will help the committee better understand and evaluate your proposal.</p>

<h2 class="hcls">Where To Submit </h2>
<p>Please submit your abstract at <a href="https://sigcomm14pd.ccs.neu.edu" rel="external">https://sigcomm14pd.ccs.neu.edu</a>. Submissions are single blind, so please include authors' names and affiliation. When submitting a poster, indicate if the submission should be considered for the SRC.</p>

