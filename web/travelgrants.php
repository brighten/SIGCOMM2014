<h1 class="hcls">ACM SIGCOMM 2014 Travel Grants</h1>


<p>This year, as in years past, the Conference Organizing Committee is working hard to provide as much travel support to as many conference attendees as possible. Travel support has been provided from many different supporting organizations. Through this support, we will be able to assist dozens of attendees.</p>

<p><b>Support is provided for a variety of different types of attendees, from graduate students to post-doctoral scholars. The amount of support depends on the type of attendee and available funding. Attendees from around the world are encouraged to apply.</b></p>

<p>Support for SIGCOMM 2014 travel grants will be available to students, post-docs, and young faculty early in their career, no matter which university you are working at. We would like to encourage all of you to apply, and we gratefully acknowledge the support we receive from the following organizations:</p>

<ul>
	<li>NSF Support: travel grants are available for full time graduate students and post-docs enrolled at U.S. Universities.</li>

	<li>ACM SIGCOMM GeoDiversity travel grants are available for non-US students and non-US attendees in their early career (at most five years of full-time, full-level, post-PhD employment) from under-represented countries, based on need, distance to the conference venue, and impact of their attendance in increasing the diversity of conference participation.</li>

	<li>AT&amp;T and Google provided funds to support a broader and larger student participation.</li>
	
	<li>The FP7 NoE FLAMINGO project also provided funds to support European students.</li>
</ul>

<p>Please consider the following important information before applying:</p>

<ul>
	<li>The application process is the same for all types of attendees and all sources of funding, and is described in the rest of this page.</li>
	<li>Travel grant awards are meant to partially cover the cost of attending and participating in SIGCOMM 2014. The only travel expenses that can be reimbursed are: air travel, hotel, and conference registration.</li>
	<li>Note that the award recipients will need to pay for the expenses ahead of time and get reimbursed after the conference.</li>

	<li>NSF-funded award recipients will need to fly with a US carrier.

	<li>The exact number of awards will depend on the availability of funds and will be determined as funding amounts are finalized.</li>
</ul>

<h2 class="hcls">Selection Criteria</h2>


<p>The following criteria will be used in the selection process:</p>

<ol>
<li>Preference will be given to applicants that do not present a paper in the conference. The rationale for this criterion is that the participation costs for a presenting author should be covered by their home institution.</li>

<li>Students presenting posters are encouraged to apply though. The rationale is to support the participation of students who do not present papers but contribute to the conference in other ways.</li>

<li>Evidence of a serious interest in networking, as demonstrated by coursework and/or project experience, will be required. Advanced students close to graduation or in the job market will also be given priority, when possible.</li>

<li>We also encourage participation of women and under-represented minorities. Special attention will be given to applicants from minority-serving institutions.</li>

<li>After giving priority to the above criteria, (co-)authors of papers may be considered but will be given lower priority. The rationale is to support people that contribute to the conference and cannot attend otherwise due to budget or other constraints. (An example is a student who is a second author but does not present and would not be able to attend the conference otherwise. Another example is a postdoc who presents work with a past affiliation or without affiliation and thus is not supported either by his/her past or current institution. In both cases, the applicant and the letter-writer should explain in their letters why it is impossible to support the applicant from other sources.)</li>

</ol>


<h2 class="hcls">AT&amp;T Future Star Sigcomm Travel Award</h2>
 
<p>The AT&amp;T Future Star Sigcomm Travel award is to encourage and support students who have serious interest in computer networking and telecommunication and work on research topics towards solving real world problems and challenges to attend the SIGCOMM conference. </p>

<h3 class="hcls">Eligibility</h3>
<ol>
<li>The applicants must be full time graduate students and post-docs studying at universities in the United States.</li>
<li>The applicants must be either US citizens or US green card holders.</li>
<li>The applicants must register and attend the SIGCOMM main conference.</li>
<li>The applicants should have serious interest in networking research and demonstrate his/her potential of being a top researcher in the fields of computer networking and telecommunication. This will be demonstrated by research projects, publications, and recommendation letters from their advisors.</li>
<li>Higher priority would be given to students who have shown their leadership on networking research towards solving real world problems/challenges.</li>
</ol>

<h3 class="hcls">Award</h3>

<p>The travel award winners will receive reimbursement to cover partial expense of attending the SIGCOMM conference. After the conference, the travel award winners shall follow the SIGCOMM travel grant procedure to reimburse their travel expense up to the awarded amount.</p>

<p>In addition, the travel award winners will receive Certificates of Recognition presented at the SIGCOMM student dinner which will be held in the evening of August 20th, 2014. The winners and their advisors will also be invited to a special award lunch (or dinner) organized by AT&amp;T Labs â€“ Research (schedule and location will be announced).</p>


