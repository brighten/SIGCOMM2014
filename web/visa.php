<h2 class=hcls>Visa Information</h2>

<p>The site <a href="http://travel.state.gov">http://travel.state.gov</a> has information about obtaining a visa for those traveling to the United States with links to websites for U.S. embassies and consulates worldwide. The embassy and consulate websites have very helpful information about procedures, timelines, communities served, required documentation, and fees. International registrants should be particularly aware and careful about visa requirements, and should plan travel well in advance.</p>

<p>All visa inquiries must be handled by ACM Headquarters. Please send your request for a letter in support of a visa application to supportletters@acm.org. Include your name, mailing address, and fax number, as well as the name of the conference you are attending. (Authors of papers/posters should also include the title). Please note that ACM does not issue formal letters of invitation to any of its conferences.


